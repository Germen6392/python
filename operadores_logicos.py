# AND
# Ejemplo:
paro_pedidosya = True
asistir_paro = False
resultado_and = paro_pedidosya & asistir_paro #resultado falso porque true and false da false
print(resultado_and)
# OR 
# Ejemplo:
paro_pedidosya = True
asistir_paro = False
resultado_or = paro_pedidosya | asistir_paro #resultado true porque true or false da true
print(resultado_or)
# NOT
# Ejemplo:
paro_pedidosya = True
asistir_paro = False
resultado_not = not paro_pedidosya # not invierte el resultado

print(resultado_not)
